#This is how If Else statements work in python

#defining variables
a = 5
b = 4

#if the value of 'a' is greater than the value of 'b'
if(a > b):
    #notice the indentation. This is required for python to work properly
    print("a is greater than b")
#if this is false, continue to the 'else'
else:
    print("a is not greater than b")

"""
OUTPUT:

a is greater than b

"""